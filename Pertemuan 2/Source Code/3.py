# 3 Perhitungan rumus fisika dari energi potensial (ep)
massa = int(input("Masukkan massa (kg): "))
gravitasi = float(9.8)
tinggi = int(input("Masukkan tinggi (meter): "))
ep = massa * gravitasi * tinggi
print("Energi potensial dengan massa", massa, ", gravitasi", gravitasi, ", dan tinggi", tinggi, "adalah", ep, "J")
